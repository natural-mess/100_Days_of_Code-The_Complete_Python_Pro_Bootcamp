# Subscripting
print("Hello"[-1]) # print last character of string "Hello" which is "o"
print("Hello"[-2]) # print letter "l"

# String
print("123" + "345")

# Integer = Whole number
print(123 + 345)

# Large Integers
print(123_456_789)

# Float = Floating Point Number
print(3.14159)

# Boolean
print(True)
print(False)
